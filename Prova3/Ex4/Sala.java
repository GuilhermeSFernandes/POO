package Ex4;

public class Sala {
    private int numSala;
    private int capMaxima;

    public Sala(int numSala, int capMaxima) {
        this.numSala = numSala;
        this.capMaxima = capMaxima;
    }

    public int getCapMaxima() {
        return capMaxima;
    }

    public int getNumSala() {
        return numSala;
    }
}
